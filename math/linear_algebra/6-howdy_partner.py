"""
this module is a function that concatenates two arrays
"""
def cat_arrays(arr1, arr2):
    """
    concatenating 2 arrays

    Args:
    - arr1 (list of ints/floats): The first array.
    - arr2 (list of ints/floats): The second array.

    Returns:
    - list: A new list concatenated arrays
    """
    return arr1 + arr2
